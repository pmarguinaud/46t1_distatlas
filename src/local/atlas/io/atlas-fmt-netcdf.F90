IF (LLEXIST) THEN
  ! Open file and check the geometry is the same as YDGRID
ELSE
  ! Create file with geometry YDGRID
ENDIF

CALL YLCOMM%FINAL ()

END SUBROUTINE OPEN

SUBROUTINE CLOSE (THIS)
CLASS (ATLAS_FMT_NETCDF_t),    INTENT (INOUT) :: THIS

TYPE (FCKIT_MPI_COMM) :: YLCOMM

YLCOMM = FCKIT_MPI_COMM ()

WRITE (*, '(" CLOSE ",A16,", MYPROC = ",I4)') " ", YLCOMM%RANK () + 1

CALL YLCOMM%FINAL ()

END SUBROUTINE CLOSE

END MODULE 

