  PROCEDURE :: GRID
  PROCEDURE :: READ    
  PROCEDURE :: WRITE   
  PROCEDURE :: OPEN    
  PROCEDURE :: CLOSE   

END TYPE ATLAS_FMT_NETCDF_t

PRIVATE

PUBLIC :: ATLAS_FMT_NETCDF_t

CONTAINS

