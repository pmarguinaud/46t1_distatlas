LOGICAL,                       INTENT (INOUT) :: LDUNDEF
REAL (KIND=JPRB),              INTENT (INOUT) :: PUNDEF

TYPE (FCKIT_MPI_COMM) :: YLCOMM

YLCOMM = FCKIT_MPI_COMM ()

! Write field

WRITE (*, '(" WRITE ",A16,", MYPROC = ",I4,L3,E12.4,I8)') CDNAME, YLCOMM%RANK () + 1, LDUNDEF, PUNDEF, SIZE (PFLDG)

CALL YLCOMM%FINAL ()

END SUBROUTINE WRITE

